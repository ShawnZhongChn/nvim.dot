----------------- @Note: Indent Blankline 配置
--- 为缩进层级添加可视化引导线，增强代码结构可读性。

--------------------------------------------------------------------------------
-- Options Components
--------------------------------------------------------------------------------

--- @Note: 获取插件配置项，包含字符样式与排除规则
--- @return table
local function _get_opts()
  return {
    -- 缩进线样式配置
    indent = {
      char = '│', -- 使用更平滑的垂直线字符
      tab_char = '│',
    },
    -- 作用域高亮配置 (跟随光标所在的 block)
    scope = {
      enabled = true,
      show_start = false, -- 不显示作用域顶部的横线
      show_end = false, -- 不显示作用域底部的横线
    },
    -- 排除不需要显示缩进线的场景
    exclude = {
      filetypes = {
        'help',
        'alpha',
        'dashboard',
        'neo-tree',
        'Trouble',
        'lazy',
        'mason',
        'notify',
        'toggleterm',
        'oil', -- 针对你使用的 oil.nvim 进行优化
      },
    },
  }
end

--------------------------------------------------------------------------------
-- Enhancement Methods
--------------------------------------------------------------------------------

-- 此插件暂无复杂的 Autocmd 或 Keymap 增强需求

--------------------------------------------------------------------------------
-- Core Logic
--------------------------------------------------------------------------------

--- @Note: 插件初始化
--- @param opts table
local function _config(_, opts)
  require('ibl').setup(opts)
end

--------------------------------------------------------------------------------
-- Plugin Spec
--------------------------------------------------------------------------------

return {
  {
    'lukas-reineke/indent-blankline.nvim',
    main = 'ibl',
    opts = _get_opts(),
    config = _config,
  },
}
